export function getCategoryById(id: string): WordCategory | undefined {
  return WORD_CATEGORIES.find((c) => c.id === id)
}

export function getRandomWord(categoryId: string, usedWords: Set<string>): { word: string; categoryName: string } {
  const cat = getCategoryById(categoryId) ?? WORD_CATEGORIES[0]
  const available = cat.words.filter((w) => !usedWords.has(w))
  const pool = available.length > 0 ? available : cat.words
  return { word: pool[Math.floor(Math.random() * pool.length)], categoryName: cat.nameAr }
}
